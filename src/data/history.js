function parseYear(year) {
  if (year && year.indexOf("前") > -1) {
    year = -year.replace(/\D/gim, "");
  }

  year = parseInt(year);
  return year;
}
function buildTimelineData(data) {
  let pre = {};
  let year = {};
  let timeline = [];

  data.forEach(item => {
    for (let i = parseYear(item.start); i <= parseYear(item.end); i++) {
      if (i < 0) {
        pre[-i] = pre[-i] || [];
        pre[-i].push(item);
      } else {
        year[i] = year[i] || [];
        year[i].push(item);
      }
      timeline[i + 3000] = timeline[i + 3000] || [];
      timeline[i + 3000].push(item);
    }
  });
  return { pre: pre, year: year, timeline };
}
